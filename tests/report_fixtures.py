"""Deterministic `MatchReport` fixtures for AAASM-4391's sample report
artifacts (`docs/samples/`) and its snapshot/schema tests.

**Why hand-built, not a live `run_match()`.** Two independent reasons:

1. `arena.integrations.adapter.FakeAgentAssemblyClient.from_trial_spec`
   always mirrors `TrialSpec.expected` exactly (see its own docstring and
   `test_reports_scoring.py`'s module docstring) — a live match under
   `AdapterChoice.FAKE` can therefore never produce a decision that diverges
   from what a trial expects, so the "losing match with one critical
   escape" sample this ticket needs cannot be produced by running a real
   match at all, only by hand-constructing the `TrialOutcome`/
   `ArenaAuditEvent`s the way `test_reports_scoring.py` and
   `test_reports_generate.py` already do.
2. `arena.runner.match.run_match` timestamps every `MatchEvent` via a direct
   `datetime.now(UTC)` call — only `generate_match_id`'s own suffix/prefix
   accepts a `now`/`unique` override, not each event's `timestamp` — so even
   the *winning* sample can't be regenerated byte-for-byte identical across
   two live runs without patching `run_match` itself, which is out of this
   ticket's scope (see AAASM-4391's own scope note: don't change
   `generate.py`/`models.py`/`markdown.py` generation logic unless fixing a
   genuine bug).

So both samples below build their `MatchResult`/`ArenaAuditEvent` inputs by
hand, fully deterministically, but load the *real* `github-maintainer-dungeon`
`ScenarioSpec`/`TrialSpec`s from `scenarios/github-maintainer-dungeon/` via
`arena.scenarios.loader.load_scenario` — so scenario/trial ids, descriptions,
severities, and `expected` decision maps are all authentic, not invented.
The attempted tool/resource/args per trial go a bit further than the literal
`raw-python-issue-triager` agent script (`agents/official/raw-python-issue-triager/main.py`)
does: rather than only the subset of `expected` that agent happens to
attempt, every fixture below attempts *every* key in each trial's `expected`
map, so the resulting sample report is a complete, illustrative record of
every decision type (`allow`/`deny`/`ask`/`drop`/`redact`) the scenario's
victory conditions are built to exercise — useful for a report a human
skims once, rather than a literal transcript of one specific agent process.

**Redaction contract.** Every audit event is written through
`append_audit_event` and read back through `read_audit_events` before being
handed to `build_report`/`generate_report` — never passed in as raw
in-memory `ArenaAuditEvent` objects — exactly as
`arena.reports.generate`'s own module docstring and `test_reports_redaction.py`
require, so `secret-leak-attempt`'s `github.issues.comment: redact` sample
event is genuinely redacted (its `args` become `REDACTION_MARKER`) before it
ever reaches Markdown/JSON rendering.
"""

from __future__ import annotations

from datetime import UTC, datetime, timedelta
from pathlib import Path

from arena.integrations.audit import ArenaAuditEvent, append_audit_event, read_audit_events
from arena.integrations.decision import DefenseDecision
