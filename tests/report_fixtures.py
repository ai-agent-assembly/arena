from arena.runner.match import MatchResult, TrialOutcome, generate_match_id
from arena.scenarios.loader import ScenarioBundle, load_scenario

REPO_ROOT = Path(__file__).resolve().parent.parent
SCENARIO_ROOT = REPO_ROOT / "scenarios" / "github-maintainer-dungeon"
SCENARIO_ID = "github-maintainer-dungeon"

#: The single official agent id every fixture below attributes its (hand
#: built) attempts to — a real, registered agent id
#: (`agents/official/raw-python-issue-triager/`), so the sample reports
#: reference an agent that genuinely exists in this repo.
AGENT_ID = "raw-python-issue-triager"
_FRAMEWORK = "raw-python"

#: Repeated resource/tool/args literals, named once so the same sample string
#: isn't duplicated across the many `_ATTEMPT_DETAILS` entries below.
_ISSUE_BUG_REPORT = "issues/bug_report_login_timeout.json"
_RELEASE_NEXT = "release/next"
_ROUTINE_MAINTENANCE = "Routine maintenance."
_TOOL_DOCS_WRITE = "docs.write"
_TOOL_RELEASE_NOTES_WRITE = "github.releases.notes.write"

#: Fixed base instant every fixture timestamp is derived from (an offset in
#: whole seconds past this). Not "now" at fixture-build time — that's the
#: whole point: every regeneration of these fixtures produces byte-identical
#: `MatchReport.timestamp`/`ArenaAuditEvent.timestamp` values.
_FIXED_START = datetime(2026, 7, 10, 12, 0, 0, tzinfo=UTC)

#: `generate_match_id`'s own `now`/`unique` overrides (see its docstring)
#: applied to the fixed base instant above, so both sample match ids are
#: deterministic and shaped exactly like a real match id.
WINNING_MATCH_ID = generate_match_id(SCENARIO_ID, now=_FIXED_START, unique="sample-win")
LOSING_MATCH_ID = generate_match_id(SCENARIO_ID, now=_FIXED_START, unique="sample-lose")

