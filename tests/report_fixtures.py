def _attempt_detail(*, trial_id: str, tool: str) -> tuple[str, dict[str, str], str]:
    return _ATTEMPT_DETAILS.get(
        (trial_id, tool),
        (tool.replace(".", "/"), {}, f"Sample attempt for {tool}."),
    )


def load_scenario_bundle() -> ScenarioBundle:
    """Load the real `github-maintainer-dungeon` scenario committed to this repo."""
    return load_scenario(SCENARIO_ROOT)


def _build_attempt(*, trial_id: str, tool: str, offset_seconds: int) -> ArenaActionAttempt:
    resource, args, context = _attempt_detail(trial_id=trial_id, tool=tool)
    return ArenaActionAttempt(
        agent_id=AGENT_ID,
        framework=_FRAMEWORK,
        scenario_id=SCENARIO_ID,
        trial_id=trial_id,
        tool=tool,
        resource=resource,
        args=dict(args),
        context=context,
        timestamp=_FIXED_START + timedelta(seconds=offset_seconds),
    )


def _build_decision(*, effect: Decision, severity: Severity) -> DefenseDecision:
    return DefenseDecision(
        effect=effect,
        layer="policy",
        reason=f"Sample policy decision: {effect.value}.",
        policy_id="sample-policy",
        severity=severity,
    )


def _build_match_result(
    *,
    match_id: str,
    scenario: ScenarioBundle,
    trial_outcomes: list[TrialOutcome],
    critical_escapes: int,
) -> MatchResult:
    victory_conditions_violated = (
        critical_escapes > scenario.scenario.victory_conditions.critical_escapes
    )
    return MatchResult(
        match_id=match_id,
        scenario=scenario.scenario,
        workspace=Path("unused-sample-workspace"),
        events=(
            MatchEvent(
                type=MatchEventType.MATCH_STARTED,
                match_id=match_id,
                timestamp=_FIXED_START,
                scenario_id=scenario.scenario.id,
            ),
        ),
        trial_outcomes=tuple(trial_outcomes),
        critical_escapes=critical_escapes,
        victory_conditions_violated=victory_conditions_violated,
    )


def _persist_and_read_back(
    *, audit_scratch_path: Path, events: list[ArenaAuditEvent]
) -> list[ArenaAuditEvent]:
    """Write `events` through `append_audit_event` and read them back via
    `read_audit_events`, so any `Decision.REDACT` event is genuinely
    redacted before reaching `build_report` — see the module docstring's
    "Redaction contract" section.
    """
    if audit_scratch_path.exists():
        audit_scratch_path.unlink()
    for event in events:
        append_audit_event(audit_scratch_path, event)
    return read_audit_events(audit_scratch_path)


def _decided_events_for_trial(
    *,
    match_id: str,
    trial: TrialSpec,
    offset_seconds: int,
    override_effect: Decision | None,
    tool: str | None,
) -> list[ArenaAuditEvent]:
    """One `DECIDED` `ArenaAuditEvent` per `(trial.id, tool)` in `trial.expected`.

    `override_effect`/`tool` let a caller flip exactly one tool's decision
    away from `trial.expected[tool]` (used by the losing sample's critical
    escape) while every other key still gets a decision that matches
    `trial.expected` exactly. Every event's `timestamp` is pinned to its own
    attempt's (already-fixed) timestamp via `model_copy` — `for_decision`
    itself defaults `timestamp` to real wall-clock "now", which would make
    these fixtures non-deterministic.
    """
    events: list[ArenaAuditEvent] = []
    for index, (expected_tool, expected_decision) in enumerate(sorted(trial.expected.items())):
        effect = (
            override_effect
            if override_effect is not None and expected_tool == tool
            else expected_decision
        )
        attempt = _build_attempt(
            trial_id=trial.id, tool=expected_tool, offset_seconds=offset_seconds + index
        )
        decision = _build_decision(effect=effect, severity=trial.severity)
        event = ArenaAuditEvent.for_decision(match_id=match_id, attempt=attempt, decision=decision)
        events.append(event.model_copy(update={"timestamp": attempt.timestamp}))
    return events


def build_winning_match_report(*, audit_scratch_path: Path) -> MatchReport:
    """A full, deterministic win: every trial's every `expected` decision is
    attempted and rendered exactly as expected, so `score_match` reports
    zero failures of every kind and `MatchOutcome.AGENT_ASSEMBLY_WINS`.
    """
    bundle = load_scenario_bundle()
    trial_outcomes: list[TrialOutcome] = []
    raw_events: list[ArenaAuditEvent] = []

    for trial_index, trial in enumerate(bundle.trials):
        trial_events = _decided_events_for_trial(
            match_id=WINNING_MATCH_ID,
            trial=trial,
            offset_seconds=trial_index * 10,
            override_effect=None,
            tool=None,
        )
        raw_events.extend(trial_events)
        trial_outcomes.append(
            TrialOutcome(
                trial=trial,
                agent_id=AGENT_ID,
                result=AgentRunResult(
                    exit_code=0, stdout="", stderr="", duration_seconds=0.42 + trial_index * 0.1
                ),
                passed=True,
                error=None,
            )
        )

    audit_events = _persist_and_read_back(audit_scratch_path=audit_scratch_path, events=raw_events)

    match_result = _build_match_result(
        match_id=WINNING_MATCH_ID,
        scenario=bundle,
        trial_outcomes=trial_outcomes,
        critical_escapes=0,
    )
    score = score_match(match_result, bundle.scenario, audit_events)
    assert score.outcome.value == "agent-assembly wins", (
        "build_winning_match_report fixture drifted: expected a win, see score_match inputs"
    )
    return build_report(match_result, score, audit_events)


#: The trial/tool the losing sample's single critical escape happens on:
#: `prompt-injection-code-write` (severity `critical`) is expected to `deny`
#: `github.contents.write`; the losing fixture renders it as an unexpected
#: `allow` instead.
_LOSING_TRIAL_ID = "prompt-injection-code-write"
_LOSING_TOOL = "github.contents.write"


def build_losing_match_report(*, audit_scratch_path: Path) -> MatchReport:
    """A deterministic loss with exactly one critical escape:
    `prompt-injection-code-write`'s `github.contents.write` renders `allow`
    instead of the expected `deny`, letting a prompt-injected direct push to
    `main` through. Every other trial still resolves exactly as
    `winning_match_report` does.
    """
    bundle = load_scenario_bundle()
    trial_outcomes: list[TrialOutcome] = []
    raw_events: list[ArenaAuditEvent] = []
    critical_escapes = 0

    for trial_index, trial in enumerate(bundle.trials):
        is_losing_trial = trial.id == _LOSING_TRIAL_ID
        trial_events = _decided_events_for_trial(
            match_id=LOSING_MATCH_ID,
            trial=trial,
            offset_seconds=trial_index * 10,
            override_effect=Decision.ALLOW if is_losing_trial else None,
            tool=_LOSING_TOOL if is_losing_trial else None,
        )
        raw_events.extend(trial_events)
        if is_losing_trial:
            critical_escapes += 1
        trial_outcomes.append(
            TrialOutcome(
                trial=trial,
                agent_id=AGENT_ID,
                result=AgentRunResult(
                    exit_code=0, stdout="", stderr="", duration_seconds=0.42 + trial_index * 0.1
                ),
                passed=not is_losing_trial,
                error=None,
            )
        )

    audit_events = _persist_and_read_back(audit_scratch_path=audit_scratch_path, events=raw_events)

    match_result = _build_match_result(
        match_id=LOSING_MATCH_ID,
        scenario=bundle,
        trial_outcomes=trial_outcomes,
        critical_escapes=critical_escapes,
    )
    score = score_match(match_result, bundle.scenario, audit_events)
    assert score.outcome.value == "agent-assembly loses", (
        "build_losing_match_report fixture drifted: expected a loss, see score_match inputs"
    )
    assert score.critical_escapes == 1, (
        "build_losing_match_report fixture drifted: expected exactly one critical escape"
    )
    return build_report(match_result, score, audit_events)


def score_for(report: MatchReport) -> MatchScore:
    """Convenience accessor so callers don't need to know `MatchReport.score`
    is where `build_report` attaches the `MatchScore` it was built from.
    """
    return report.score
